    public void HienThiBang() {
        tbl.removeAll(tbl);

        //kết nối cơ sở dữ liệu
        try {
            cnn = dbcon.open();
            if (cnn != null) {
                //khai báo câu lệnh sql
                String sqlcmd = "SELECT * FROM lophoc";
                //tạo đối tượng repareStatement
                PreparedStatement ps = cnn.prepareStatement(sqlcmd);
                //Tạo rs
                ResultSet rs = ps.executeQuery();
                //đọc dữ liệu từ rs ra bảng
                while (rs.next()) {
                    //tạo một hàng trong bảng
                    Vector row = new Vector();
                    //lấy dữ liệu của rs đổ vào row
                    row.add(rs.getString("msLop"));
                    row.add(rs.getString("tenLop"));
                    row.add(rs.getString("GiaovienCN"));
                    //thêm hàng vào bảng
                    tbl.add(row);
                }
                //hiển thị dữ liệu lên bảng
                tblLop.setModel(new DefaultTableModel(tbl, tblTile));
                cnn.close();

            } else {
                JOptionPane.showMessageDialog(null, "Lỗi kết nối csdl");
            }
        } catch (SQLException ex) {
            Logger.getLogger(QLLop.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
