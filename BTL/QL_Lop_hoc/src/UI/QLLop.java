    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        //lẫy dữ liệu từ form về 
        String ma = txtMalop.getText();
        String ten = txtTenlop.getText();
        String gv = txtGVCN.getText();
        try {
            //cập nhật dữ liệu vào csdl
            cnn = dbcon.open();
            if (cnn != null) {
                //tạo câu lệnh sql
                String sqlcmd = "UPDATE lophoc SET tenLop= '"
                        + ten + "',GiaovienCN = '"
                        + gv + "' WHERE msLop = '" + ma + "'";
                //tạo PreparedStatement
                PreparedStatement ps = cnn.prepareStatement(sqlcmd);
                //thực thi lệnh
                ps.execute();
                JOptionPane.showMessageDialog(null, "Cập nhật thành công!");
                cnn.close();//đóng kết nối
                HienThiBang();
            } else {
                JOptionPane.showMessageDialog(null, "Lỗi Kết nối CSDL");
            }
        } catch (Exception e) {

        }


    }//GEN-LAST:event_btnSuaActionPerformed

