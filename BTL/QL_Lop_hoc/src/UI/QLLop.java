            //mở kết nối csdl
            cnn = dbcon.open();
            if (cnn != null) {
                //tạo câu lệnh sql
                String sqlcmd = "Insert into lophoc values('" + ma + "','" + ten + "','" + gv + "')";
                //tạo preparationStatement
                PreparedStatement ps = cnn.prepareStatement(sqlcmd);
                //thực hiện thêm
                ps.execute();
                JOptionPane.showMessageDialog(null, "Đã thêm thành công!");
                //hiển thị lại bảng
                HienThiBang();
                cnn.close();//đóng kết nối
            } else {
                JOptionPane.showMessageDialog(null, "Lỗi kết nối csdl");
            }

